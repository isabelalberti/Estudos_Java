public class CalculaMinhaIdade {
     public static void main (String [] args) {
         int anoAtual = 2021;
         int anoQueNasci = 1991;
         int idade = anoAtual - anoQueNasci;
         String nome = "Isabel";

         System.out.println (
            "                   Olá " + nome + ", tudo bem?" +
            "\n                Sua idade atual é " + idade + " anos =) " +
            "\n              Que bom que você ainda é novinha." +
            "\n Logo logo seu aniversário vai chegar e mais experiente ficará."
         );
     } 
}
